                            <a href="MainpageTekn(11).php">Twitch, Amazon, hingga Reddit Sempat Tak Bisa Diakses</a>
                            <p>Sejumlah situs dan layanan online populer mendadak mengalami gangguan secara bersamaan pada sore ini, Selasa (8/6/2021), di antaranya Twitch, Amazon, Reddit, Spotify, hingga portal berita seperti The New York Times.</p>
                            <p>Pantauan KompasTekno di DownDetector, situs-situs dan layanan online tersebut mulai banyak dilaporkan mengalami gangguan mulai sekitar pukul 16.30 WIB. Keluhan memuncak sekitar pukul 17.00 WIB.</p>
                            <p>KompasTekno sempat tidak bisa membuka situs Twitch, Quora, dan Reddit. Tampilan laman tidak bisa dimuat dan hanya menampilkan pesan singkat berbunyi "connection failure" atau "Error 503 Service Unavailable".</p>
                            <p>Keluhan down di DownDetector berangsur menurun setelah pukul 17.00 WIB. Kini ssebagian dari situs-situs tadi sudah bisa diakses kembali.</p>
                            <p>Penyebab dari gangguan ini diketahui berasal dari pemadaman pada layanan content delivery network (CDN) milik penyedia layanan cloud yang berbasis di AS, Fastly.</p>
                            <p>CDN merupakan jaringan distribusi yang berfungsi untuk menyimpan cache data website di server regional sehingga bisa diakses dengan lebih cepat.</p>
                            <p>Namun, karena adanya pemadaman, Fastly tak bisa dalam menyediakan layanan kepada para kliennya. Hal ini mengakibatkan tumbangnya sejumlah situs yang mengandalkan layanan Fastly.</p>
                            <p>Dihimpun KompasTekno dari 9to5Mac, Selasa (8/6/2021), meski relatif tak lama, gangguan interent kali ini terjadi secara global dan berdampak luas. Bahkan situs web pemerintah Inggris juga sempat terganggu.</p>
                            <p>Di situsnya, Fastly mengakui bahwa memang terjadi gangguan yang berlangsung selama lebih kurang 45 menit sebelum akhirnya berhasil diperbaiki.</p>
