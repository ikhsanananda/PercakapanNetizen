                            <a href="MainpageLife(8).php">Susu Dianggap Makanan Mahal, Konsumsi Susu di Indonesia Rendah</a>
                            <p>Susu mengandung berbagai zat gizi penting, mulai dari protein, lemak, karbohidrat, mineral, dan vitamin. Namun, konsumsi susu di Indonesia masih jauh tertinggal dibandingkan negara tetangga, apalagi negara maju.</p>
                            <p>Konsumsi susu masyarakat Indonesia hanya 16,27 kilogram per kapita per tahun. Jumlah itu termasuk terendah jika dibandingkan dengan sejumlah negara di Asia Tenggara. Bandingkan dengan Malaysia yang mencapai 36,2 kg per kapita, atau Thailand 22,2 kg per kapita.</p>
                            <p>Banyak faktor yang menyebabkan konsumsi susu di Indonesia masih jauh tertinggal dengan negara lain.</p>
                            <p>Tim Percepatan Pencegahan Anak Kerdil (Stunting)/TP2AK, Suprayoga Hadi mengatakan, rendahnya konsumsi susu terutama dilihat dari keterjangkauan. Susu masih dilihat sumber makanan mahal atau mewah (luxurious goods).</p>
                            <p>“Susu dilihat sebagai luxurious goods. Agak mahal dalam konteks ini dipersepsikan sama dengan daging. Tapi, bagaimana pun perlu ada semacam kampanye, selain substitusi produk hewani lainnya,” kata Suprayoga dalam siaran pers.</p>
                            <p>Menurutnya perlu waktu cukup panjang untuk bisa mengubah mind set bahwa susu produk yang mewah.</p>
                            <p>Padahal, susu merupakan sumber gizi yang praktis karena bentuknya cair sehingga mudah diasup, gizi relatif lebih lengkap, dan kualitas komplementari yang tidak bisa disubstitusi oleh pangan nabati. Untuk menumbuhkan kesadaran minum susu masih harus terus dilakukan edukasi dan juga kampanye.</p>
                            <p>“Perlu ada semacam kampanye, selain substitusi produk hewani lainnya yang bisa menggantikan susu atau ada public campaign mengenai susu dan produk turunannya,” katanya.</p>
                            <p><b>Hormon pertumbuhan</b></p>
                            <p>Dokter Spesialis Nutrisi dan Penyakit Metabolik pada Anak Prof. Dr. dr. Damayanti Rusli Sjarif, Sp.A(K) mengatakan, terkait konsumsi protein hewani ini masih kurang sosialisasi.</p>
                            <p>Ia lalu menyontohkan perubahan buku KIA yang sudah memasukkan protein hewani ke dalam menu MPASI yang masih belum disosialisasikan secara massal.</p>
                            <p>“Protein hewani tidak hanya didapat dari daging, bisa yang lebih murah seperti ikan, telur, ayam, termasuk susu,” katanya.</p>
                            <p>Susu merupakan sumber protein yang baik untuk tinggi badan karena kasein dalam susu berpengaruh terhadap hormon pertumbuhan.</p>
                            <p>“Sumber protein yang terbaik untuk tinggi badan itu sebenarnya adalah susu. Lalu ada telur, itu setara dengan susu. Kemudian ayam dan ikan,” kata Damayanti.</p>
                            <p>Senada dengan Damayanti, Ahli Nutrisi dari Fakultas Kedokteran Universitas Indonesia Prof. Dr. dr. Saptawati Bardosono, MSc mengatakan, susu dibutuhkan untuk melengkapi asupan zat gizi yang belum terpenuhi dari makanan sehari-hari.</p>
                            <p>“Saat bayi sampai usia 24 bulan diutamakan pemberian ASI seuai anjuran WHO. Setelah 24 bulan, susu bisa dikonsumsi untuk melengkapi asupan zat gizi yang belum terpenuhi dari makanan saja,” kata Saptawati.</p>
